// Tüm markaları al (unique)
export const getAllBrands = (): string[] => {
  const brands = [...new Set(ALL_TURBINES.map(t => t.brand))];
  return brands.sort();
};

// Belirli bir markaya ait modelleri al (unique)
export const getModelsByBrand = (brand: string): string[] => {
  const models = [...new Set(
    ALL_TURBINES
      .filter(t => t.brand === brand)
      .map(t => t.model)
  )];
  return models.sort();
};

// Belirli marka ve modele göre türbinleri filtrele
export const filterTurbines = (
  brands: string[],
  models: string[],
  minCapacity: number,
  maxCapacity: number
): Turbine[] => {
  return ALL_TURBINES.filter(t => {
    const brandMatch = brands.length === 0 || brands.includes(t.brand);
    const modelMatch = models.length === 0 || models.includes(t.model);
    const capacityMatch = t.capacity >= minCapacity && t.capacity <= maxCapacity;
    return brandMatch && modelMatch && capacityMatch && t.capacity > 0;
  });
};

// Unique türbinleri al (aynı marka-model-kapasite kombinasyonu bir kez)
export const getUniqueTurbines = (turbines: Turbine[]): Turbine[] => {
  const seen = new Set<string>();
  return turbines.filter(t => {
    const key = `${t.brand}-${t.model}-${t.capacity}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
};

// Min ve max kapasite değerlerini al
export const getCapacityRange = (): { min: number; max: number } => {
  const capacities = ALL_TURBINES.filter(t => t.capacity > 0).map(t => t.capacity);
  return {
    min: Math.min(...capacities),
    max: Math.max(...capacities)
  };
};

